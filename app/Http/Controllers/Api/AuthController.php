<?php

namespace App\Http\Controllers\Api;

use App\Http\Controllers\Controller;
use App\Models\User;
use Illuminate\Http\Request;
use Illuminate\Support\Facades\Hash;
use Illuminate\Validation\ValidationException;

class AuthController extends Controller
{
    /**
     * Login and get API token
     */
    public function login(Request $request)
    {
        $request->validate([
            'email' => 'required|email',
            'password' => 'required',
        ]);

        $user = User::where('email', $request->email)->first();

        if (!$user || !Hash::check($request->password, $user->password)) {
            throw ValidationException::withMessages([
                'email' => ['The provided credentials are incorrect.'],
            ]);
        }

        // BUG 3 FIX: Restrict this endpoint to admin role only
        if ($user->role !== 'admin') {
            throw ValidationException::withMessages([
                'email' => ['Access denied. Use the appropriate login endpoint for your role.'],
            ]);
        }

        // Delete old tokens
        $user->tokens()->delete();

        // Create new token with the user's ACTUAL role (not hardcoded admin)
        $token = $user->createToken('mobile-app', ['role:' . $user->role])->plainTextToken;

        return response()->json([
            'success' => true,
            'token' => $token,
            'user' => [
                'id' => $user->id,
                'name' => $user->name,
                'email' => $user->email,
                'role' => $user->role,
                'wallet_balance' => $user->wallet_balance,
                'area_id' => $user->area_id,
            ],
        ]);
    }

    /**
     * Logout and revoke token
     */
    public function logout(Request $request)
    {
        $request->user()->currentAccessToken()->delete();

        return response()->json([
            'success' => true,
            'message' => 'Logged out successfully',
        ]);
    }

    /**
     * Get authenticated user
     */
    public function me(Request $request)
    {
        $user = $request->user();

        return response()->json([
            'success' => true,
            'user' => [
                'id' => $user->id,
                'name' => $user->name,
                'email' => $user->email,
                'role' => $user->role,
                'wallet_balance' => $user->wallet_balance,
                'area_id' => $user->area_id,
            ],
        ]);
    }
}
